    REAL, DIMENSION(2*n_beams) :: arg2d
    REAL :: temp
! EXECUTION
! Initialize the mass value
    mass = 0
    massd = 0.0
    marginsd = 0.0
    kid = 0.0
! Loop over every element to compute contributions of mass and stress
