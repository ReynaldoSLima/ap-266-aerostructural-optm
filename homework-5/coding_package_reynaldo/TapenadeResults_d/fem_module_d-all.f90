    REAL :: sigma1, sigma2, tau, sigma_vm1, sigma_vm2
    REAL :: sigma1d, sigma2d, taud, sigma_vm1d, sigma_vm2d
    REAL :: margin_min, margin_sum
    REAL :: margin_sumd
    INTEGER :: n_dofs, jj
    INTRINSIC SQRT
    INTRINSIC EXP
    INTRINSIC SUM
    INTRINSIC LOG
    REAL :: arg1
    REAL :: arg1d
    REAL, DIMENSION(2*n_beams) :: arg10
    REAL, DIMENSION(2*n_beams) :: arg10d
